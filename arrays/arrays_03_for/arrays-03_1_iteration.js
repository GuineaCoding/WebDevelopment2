// Create an array called `people` with initial elements: 'Greg', 'Mary', 'Devon', 'James'
const people = ['Greg', 'Mary', 'Devon', 'James'];

// Iterate over the `people` array using a for loop and print each element to the console
for (let t = 0; t < people.length; t++) {
    console.log(people[t]);
}

// Iterate over the `people` array in reverse order using a for loop and print each element to the console
for (let s = people.length - 1; s >= 0; s--) {
    console.log(people[s]);
}
